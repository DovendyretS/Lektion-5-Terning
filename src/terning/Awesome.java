package terning;

public class Awesome {

    String det, her, er, awesome;

    public Awesome(String det, String her, String er, String awesome) {

        this.det = det;
        this.her = her;
        this.er = er;
        this.awesome = awesome;
    }
    public String toString()
    {

        return det + her + er + awesome;
    }

}
